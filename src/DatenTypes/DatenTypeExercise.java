package DatenTypes;

public class DatenTypeExercise {
    public static void main(String[] args) {
         /**
         * Schritt 1: String-Variable
         * Deklariere eine Variable,
         * um den Namen einer Person zu speichern,
         * weise ihr einen Wert zu und gib sie in der Konsole aus.
         */

        String name = "Yuliia Bezkorovaina";
        System.out.println("Ich bin " + name);

        /**
         * Schritt 2: Integer-Variable
         * Deklariere eine Variable, um das Alter einer Person zu speichern,
         * weise ihr einen Wert zu und gib sie in der Konsole aus.
         */

        int alt = 40;
        System.out.println("Ich bin " + alt);

        /**
         * Schritt 3: Double-Variable
         * Deklariere eine Variable,
         * um die Körpergröße einer Person in Metern zu speichern (z. B. 1,75),
         * weise ihr einen Wert zu und gib sie aus.
         */

        double gross = 1.64;
        System.out.println("Mein Gross ist " + gross + " sm");

        /**
         * Schritt 4: Boolean-Variable
         * Deklariere eine Variable, die angibt,
         * ob eine Person ein Student ist (true oder false),
         * weise ihr einen Wert zu und gib sie aus.
         */
        boolean isStudent = true;
        System.out.println(isStudent + ", ich bin eine Studentin");

    }

}
